package model;

/**
 * Information of boat.
 */
public class Boat {
  private String type;
  private int length;

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public int getLength() {
    return length;
  }

  public void setLength(int length) {
    this.length = length;
  }
}
